    function showPreview(obj){
        if(obj.files && obj.files[0]){
            var reader = new FileReader();
            reader.onload = function (e) {
            $("#userImage_img").attr('src', e.target.result);
        }
            reader.readAsDataURL(obj.files[0]);
        }
    }
